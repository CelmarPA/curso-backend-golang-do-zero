package main

import (
	"fmt"
	"strings"
)

func PartList(arr []string) string {
	var parts []string
	
	for i := 1; i < len(arr); i++ {
		left := strings.Join(arr[:i], " ")    // Junta a primeira parte com espaÃ§o
		right := strings.Join(arr[i:], " ")   // Junta a segunda parte
		part := fmt.Sprintf("(%s, %s)", left, right)
		parts = append(parts, part)
	}

	return strings.Join(parts, "")
}

func main() {
	fmt.Println(PartList([]string{"I", "wish", "I", "hadn't", "come"}))
}
